package model;

public class Canco {
	String nom;
	String genere;
	String album;
	String artista;
	String path;
	
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	int estrelles;
	int reproduccions;
	
	
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public String getGenere() {
		return genere;
	}
	public void setGenere(String genere) {
		this.genere = genere;
	}
	public String getAlbum() {
		return album;
	}
	public void setAlbum(String album) {
		this.album = album;
	}
	public String getArtista() {
		return artista;
	}
	public void setArtista(String artista) {
		this.artista = artista;
	}
	public int getEstrelles() {
		return estrelles;
	}
	public void setEstrelles(int estrelles) {
		this.estrelles = estrelles;
	}
	public int getReproduccions() {
		return reproduccions;
	}
	public void setReproduccions(int reproduccions) {
		this.reproduccions = reproduccions;
	}
	
	
	

}
